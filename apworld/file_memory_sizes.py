def update_room_default_definitions_post_fill(all_regions, multiworld_locations, randomized_enemy_data, all_item_table, all_enemies, restricted_enemies, restricted_enemy_rooms, random_generator):
    """
    Update room_default_definitions for rooms without enemies (enemy rooms are handled during randomization).

    Args:
        all_regions: Dictionary of all region definitions
        multiworld_locations: Multiworld location data to get randomized items
        randomized_enemy_data: Dictionary of randomized enemy data by room_id
        all_item_table: Dictionary of all item data with entity_id mappings
        all_enemies: List of all available enemies for randomization
        restricted_enemies: Set of restricted enemy IDs
        restricted_enemy_rooms: Set of room IDs with restricted enemies
        random_generator: Random generator for rerolling enemies

    Returns:
        Dictionary with room_id as key and dict containing 'files' and 'under_budget' as values
    """
    room_file_data = {}

    # Create mapping of item names to entity IDs for faster lookup
    item_name_to_entity = {}
    for item_name, item_data in all_item_table.items():
        if hasattr(item_data, "entity_id") and item_data.entity_id is not None:
            item_name_to_entity[item_name] = item_data.entity_id

    for region_name, region_data in all_regions.items():
        if not hasattr(region_data, "room_id") or not hasattr(region_data, "room_default_definitions"):
            continue

        # Skip rooms with enemies - they were already processed during enemy randomization
        if (
            hasattr(region_data, "enemies") and region_data.enemies and hasattr(region_data, "room_id") and region_data.room_id in randomized_enemy_data and randomized_enemy_data[region_data.room_id]
        ):  # Only if there's actual enemy data
            # Just record the data for rooms that were already processed
            room_id = region_data.room_id
            total_size = 0
            for file_id in region_data.room_default_definitions:
                if file_id in file_sizes:
                    total_size += file_sizes[file_id]

            room_file_data[room_id] = {"files": list(region_data.room_default_definitions), "under_budget": total_size <= MINIMUM_SAFE_BUDGET, "total_size": total_size}
            continue

        room_id = region_data.room_id
        current_files = set(region_data.room_default_definitions)

        # Check if this room has any locations or enemies defined
        has_locations = hasattr(region_data, "locations") and region_data.locations
        has_enemies = hasattr(region_data, "enemies") and region_data.enemies

        # If the room has no locations and no enemies, keep original definitions unchanged
        if not has_locations and not has_enemies:
            total_size = 0
            for file_id in current_files:
                if file_id in file_sizes:
                    total_size += file_sizes[file_id]

            room_file_data[room_id] = {"files": list(current_files), "under_budget": total_size <= MINIMUM_SAFE_BUDGET, "total_size": total_size}
            continue

        # Get randomized items for this region's locations
        region_items_entities = []
        if hasattr(region_data, "locations"):
            for location_logic in region_data.locations:
                # Find the multiworld location that matches this logic location
                for multiworld_location in multiworld_locations:
                    if hasattr(multiworld_location, "name") and multiworld_location.name.endswith(f" - {location_logic.name}") and multiworld_location.item:

                        item_name = multiworld_location.item.name
                        if item_name in item_name_to_entity:
                            region_items_entities.append(item_name_to_entity[item_name])
                        break

        # Process randomized items - add required files
        for entity_id in region_items_entities:
            if entity_id in entities_dict:
                required_file = entities_dict[entity_id][0]
                current_files.add(required_file)

        # Remove unused pickup files
        used_pickup_files = set()
        for entity_id in region_items_entities:
            if entity_id in entities_dict:
                file_id = entities_dict[entity_id][0]
                if file_id in PICKUP_FILES:
                    used_pickup_files.add(file_id)

        # for pickup_file in PICKUP_FILES:
        #     # Never remove 0x2B, but other pickup files can be removed if unused
        #     if pickup_file != 0x02B and pickup_file not in used_pickup_files and pickup_file in current_files:
        #         current_files.discard(pickup_file)

        # Remove unused enemy files (these rooms shouldn't have enemy files anyway)
        # for enemy_file in ENEMY_FILES:
        #     if enemy_file in current_files:
        #         current_files.discard(enemy_file)

        # Calculate memory budget
        total_size = 0
        for file_id in current_files:
            if file_id in file_sizes:
                total_size += file_sizes[file_id]

        under_budget = total_size <= MINIMUM_SAFE_BUDGET

        # Raise exception if room exceeds memory budget (shouldn't happen for non-enemy rooms)
        if not under_budget:
            files_hex = [hex(file_id) for file_id in current_files]
            raise RuntimeError(
                f"Room {hex(room_id)} in region '{region_name}' exceeds memory budget! "
                f"Total size: {total_size} bytes, Budget limit: {MINIMUM_SAFE_BUDGET} bytes, "
                f"Overage: {total_size - MINIMUM_SAFE_BUDGET} bytes. "
                f"Room ID: {hex(room_id)}, Files being loaded: {' '.join(files_hex)}"
            )

        # Update the region's room_default_definitions
        region_data.room_default_definitions = list(current_files)

        # Store result for slot data
        room_file_data[room_id] = {"files": list(current_files), "under_budget": under_budget, "total_size": total_size}

    return room_file_data


