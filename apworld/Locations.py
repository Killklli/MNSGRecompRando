"""Location definitions for Mystical Ninja Starring Goemon (MN64)."""

from typing import Dict

# Base ID for all MN64 locations
BASE_ID = 6474000


def get_location_name_to_id() -> Dict[str, int]:
    """Build a mapping of location name â†’ AP ID, derived from Logic region definitions.

    Location names are generated by the same algorithm used in create_game_regions.
    IDs are assigned in alphabetical order starting at BASE_ID.
    """
    from .Regions import build_location_names, import_region_logic

    all_regions = import_region_logic()
    names = build_location_names(all_regions)
    return {name: BASE_ID + i for i, name in enumerate(sorted(names))}
